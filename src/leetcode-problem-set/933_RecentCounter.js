var RecentCounter = function () {
  this.queue = [];
};

/**
 * @param {number} t
 * @return {number}
 */
RecentCounter.prototype.ping = function (t) {
  this.queue.push(t);
  const range = [t - 3000, t];

  while (this.queue.length && this.queue[0] < range[0]) {
    this.queue.shift();
  }
  return this.queue.length;
};

